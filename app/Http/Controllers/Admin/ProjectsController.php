    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $project = Project::find($id);

        $project->applicants_num = $request->input('applicants_num');
        $project->trainees_num = $request->input('trainees_num');
        $project->centers_num = $request->input('centers_num');
        $project->courses_no = $request->input('courses_no');

        $project->save();

        return redirect('/admin/projects/1/edit');
    }

